let primeiroNumero = prompt('Digite um número:')
let segundoNumero = prompt('Digite mais um número:')

primeiroNumero = parseInt(primeiroNumero);
segundoNumero = parseInt(segundoNumero);

alert("A divisão dos números digitados é: " + (primeiroNumero / segundoNumero));
alert("A multiplicação dos números digitados é: " + (primeiroNumero * segundoNumero));
alert("A subtração dos números digitados é: " + (primeiroNumero - segundoNumero));
alert("A soma dos números digitados é: " + (primeiroNumero + segundoNumero));